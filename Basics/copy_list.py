# Using the copy() method:
list1 = ['apple' , 'orange', 'cherry']
newlist = list1.copy()
print(newlist)

# Using the list() method:

list2 = ['apple' , 'orange', 'cherry']
newlist2 = list(list2)
print(newlist2)

# Using the slice Operator:

list3 = ['math' , 'nepali', 'science']
newlist3 = list3[:]
print(newlist3)